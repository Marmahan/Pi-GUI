//blink when the mouse is over the picture
function blinkover() { 
  var elem = document.getElementById("blinkingman");   
  elem.classList.toggle("blink-image");
}

//stop blinking when mouse is not over the picture
function blinkout() {
  var elem = document.getElementById("blinkingman");   
  elem.classList.toggle("blink-image");
}

